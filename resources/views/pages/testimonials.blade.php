						<h4><strong>Mwehembana reclaims his land from a corrupt neighbor</strong></h4><br>
						<p>After many years of trying to get back his land, Samson Mwehembana, had  reached a point  of giving  up  his  fight.  Mwehembana,  the  89-year-old  retired  veteran,  has  been  fighting  in  court and spending his daily life in local government offices since 2015, to reclaim his land which was confiscated by his rich and powerful neighbor who, he claims, used his money and influence to bribe  the  courts  to  rule  in  his  favor.  Mzee  Mwehembana  lives  in  Makulu  ward  in  Dodoma Municipality Council.</p>

						<p>Land conflicts are among the common problems in different parts of Tanzania, causing disputes within  community  members  leading  to  confrontations  that  may  result  to  relatives  killing  each other.  These  disputes  in  the  communities  lead  to  insecurity  causing  people  not  to  engage  in their  daily  activities  in  fear  of  their  lives.  This,  in  the  long  run,  affects  the  economy  of  the community in question and, hence, the national output as a whole.</p>

						<p>Mwehembana’s  struggles  became  difficult  because  some  local  authority  officials  are  rooted into  these  land  disputes  and  benefit  from  them  because  they  are  bribed  by  those  who  have money.  This  means  people  like  mzee  Mwehembana  are  deprived  of  their  rights  by  corrupt leaders. 

						After   numerous   attempts   to   claim   his   rightful   property   at   different   levels   from   village leadership to ward officials and, to what he thought was the last resort and savior, the Primary Court  Magistrate.   At  last,  the  alleged  corrupt  rich  guy   in the  ward  won  the  case  and Mwehembana lost, to nobody’s surprise.
						</p>

						<p>One evening, Mwehembana got a visitor at his home, one of his longtime friends. He found the senior  citizens,  mzee  Mwehembana  and  his  lovely  wife,  with  fresh  wounds  of  their  loss  at  the Primary Court. They narrated the sad story to the visitor who, to their surprise, was amused by the outcome of the case! He told them about Makulu Ward Paralegals and how they might help them reclaim what they had considered gone and dusted.
						
						"This  friend  of  mine  advised  us  to  go  and  seek  Makulu  Paralegals’  help  in  our  bid  to  change what we thought was lost hope. I went to them narrated my case to them they agreed to help us," says the old man.
						</p>

						<p>The paralegals reviewed all the documents from  the Primary Court and advised Mwehembana  to file an appeal at the District Court. With the help of these paralegals, the court ruled in the old man’s favor and he got his land back and was compensated a total amount of TShs 380,000.

						Mwehembana  was so  excited  with  this  outcome  saying, "These  boys  are  doing  a  great  deal  of service  to  our  community  as  they  are  helping  poor  people  like  me with  legal  problems  in  our village."
