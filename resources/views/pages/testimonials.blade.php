						<h4><strong>Church opposes marriage ceremony of a 16-year old girl </strong></h4><br>
						<p>Parents and relatives had planned to marry an 16-year old girl to an old man in Sikonge district, Tabora region. The parents had presented their request to the Adventists church in the villages and, initially, the church was willing to give in to their wish. 

						Forced  early  marriages  is  a  common  practice  in  most  communities  in  Tanzania,  whereby  most girls  are  deprived  of  their  rights  such  as  the  right  to  education,  and  freedom  of  choice.  This  is mostly due to lack of education on issues about human rights and legal aid.
						</p>

						<p>In  this  case,  Sikonge  Paralegals  took  the  initiative  to  stop  this  marriage  by  empowering  the community members to pressurize the church to stop the marriage taking place.

						The  legal  aid  providers  heard  about  the  marriage  while  conducting  one  of  their  sessions  of public  awareness  on  women  and  human  rights.  They  overheard  the  community  members talking about the marriage of the 16-year old girl which was to take place in the church.
						</p>

						<p>The  legal  aid  providers  heard  about  the  marriage  while  conducting  one  of  their  sessions  of public  awareness  on  women  and  human  rights.  They  overheard  the  community  members talking about the marriage of the 16-year old girl which was to take place in the church.

						"We decided to intervene and stop this marriage as it was violating the rights of the young girl who was not willing to marry an old man, says Nelson Jackson, the Sikonge district Paralegal.
						</p>

						<p>To  start  with,  the  paralegals  approached  the  girl’s  parents  and  enlightened  them  on  what  the law  says  about  a  girl  aged  below  18  in  reference  to  the  Marriage  Act,  1971.  The  parents  were difficult  to  persuade  as  they  stuck  with  their  decision  to  marry  their  young  daughter  claiming she was idle so marrying her was the only solution.

						One  day,  as  the  congregation  was  proceeding  with  regular  prayers  service  at  the  church, Jackson asked the pastor to give him few minutes to educate church members about marriage and  what  the  law  says  about  it. The  pastor  agreed  and  gave  him  the  floor.  The  paralegal addressed  the  church  members  about  the  dangers  of  early  marriages  referring  to  the  16-year old girl. 
						</p>

						<p>The church finally came to a fair conclusion deciding that they won’t proceed with the marriage ceremony  and  messages  was  delivered  to  other  churches  in  the  village  to  stop  and  condemn the practice.</p>
 						
